	// Time complexity with memoization - O(n^2)
	// for each row and col, we will compute the result once O(n^2).
	// We will store it in memo and use it later in O(1) time.
	// Space complexity with memoization - O(n^2)
	// The recursion tree depth is O(n) and memo uses O(n^2)
	// Time complexity without memoization - O(n*3^n)
	// There is 3-ary recursion tree for each cell from 1st row towards last row.
	// There are 3 possibilities in each cell and there are totoal n cells
	// 3*3*3...3 n times or 3^(n-1) for n cells in 1st row.
	// At each level there are approx 3^level nodes
	// Space complexity without memoization - O(n), for recursive stack space.
	public static int minFallingPathSumMemoized(int[][] matrix) {
		int n = matrix.length;
		int[][] dp = new int[n][n];
		for (int[] arr : dp) {
			Arrays.fill(arr, Integer.MAX_VALUE);
		}
		int minSum = Integer.MAX_VALUE;
		for (int i = 0; i < n; i++) {
			int sum = recursive(0, i, n, matrix, dp);
			minSum = Math.min(minSum, sum);
		}
		return minSum;
	}

	private static int recursive(int i, int j, int n, int[][] matrix, int[][] dp) {
		if (j < 0 || j >= n) {
			return Integer.MAX_VALUE;
		}
		if (i == n - 1) {
			return matrix[i][j];
		}
		if (dp[i][j] != Integer.MAX_VALUE) {
			return dp[i][j];
		}
		int left = recursive(i + 1, j - 1, n, matrix, dp);
		int mid = recursive(i + 1, j, n, matrix, dp);
		int right = recursive(i + 1, j + 1, n, matrix, dp);
		return dp[i][j] = matrix[i][j] + Math.min(left, Math.min(mid, right));
	}

	// Time complexity - O(n^2+n) or O(n^2)
	// Space complexity - O(n^2)
	private static int minFallingPathSum2DDP(int[][] matrix) {
		int n = matrix.length;
		int[][] dp = new int[n + 1][n + 1];
		for (int i = n - 1; i >= 0; i--) {
			// Current row value needs the value from 0 to n-1 values
			// When the next row is checked it's good to have 0 to n values
			// for last row only. Post that we can only have n total values.
			for (int j = 0; j < n; j++) {
				if (j != 0 && j != n - 1) {
					dp[i][j] = matrix[i][j] + Math.min(dp[i + 1][j - 1], Math.min(dp[i + 1][j], dp[i + 1][j + 1]));
				} else if (j != 0 && j == n - 1) {
					dp[i][j] = matrix[i][j] + Math.min(dp[i + 1][j - 1], dp[i + 1][j]);
				} else {
					dp[i][j] = matrix[i][j] + Math.min(dp[i + 1][j], dp[i + 1][j + 1]);
				}
			}
		}
		int minSum = Integer.MAX_VALUE;
		for (int i = 0; i < n; i++) {
			minSum = Math.min(minSum, dp[0][i]);
		}
		return minSum;
	}

	// Time complexity - O(n^2+n) or O(n^2)
	// Space complexity - O(n)
	private static int minFallingPathSum1DDP(int[][] matrix) {
		int n = matrix.length;
		int[] dp = new int[n + 1]; // n+1 size only for nth row.
		for (int i = n - 1; i >= 0; i--) {
			int[] curr = new int[n];
			for (int j = 0; j < n; j++) {
				if (j != 0 && j != n - 1) {
					curr[j] = matrix[i][j] + Math.min(dp[j - 1], Math.min(dp[j], dp[j + 1]));
				} else if (j != 0 && j == n - 1) {
					curr[j] = matrix[i][j] + Math.min(dp[j - 1], dp[j]);
				} else {
					curr[j] = matrix[i][j] + Math.min(dp[j], dp[j + 1]);
				}
			}
			// size of dp changes from n+1 to n post nth row.
			dp = curr;
		}
		int minSum = Integer.MAX_VALUE;
		for (int i = 0; i < n; i++) {
			minSum = Math.min(minSum, dp[i]);
		}
		return minSum;
	}

