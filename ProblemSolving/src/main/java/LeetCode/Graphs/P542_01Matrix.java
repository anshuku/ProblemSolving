	// Dynamic Programming:
	// The distance of a cell from 0 can be calculated if we know the nearest
	// distance for all the neighbors, in this case the distance if the minimum
	// distance of any neighbor + 1. This is where DP helps. From each 1, the
	// minimum path to 0 could be in any direction. So, we check all the 4
	// directions. In one iteration from top to bottom, we can check left and top
	// directions, and we need another iteration from botom to top for right and
	// bottom directions. Algo:
	// Itearate over the matrix from top to bottom-left to right: Update
	// dist[i][j] = min(dist[i][j], min(dist[i][j-1], dist[i-1][j])+1)
	// i.e., minimum of the current dist and distance from top or left neightbor+1,
	// this would've been already calculated previously in the current iteration.
	// Now, we do back iteration similarly: from bottom to top-right to left: Update
	// dist[i][j] = min(dist[i][j], min(dist[i][j+1], dist[i+1][j])+1)
	// i.e., minimum of current dist and distances calculated from bottom and right
	// neighbors, that'd be already available in current iteration.
	// Time complexity - O(r*c), we perform 2 passes over the matrix and each pass
	// requires O(r*c) time.
	// Space complexity - O(1).
	private static int[][] updateMatrixDP(int[][] mat) {
		int m = mat.length;
		int n = mat[0].length;

		int[][] result = new int[m][n];
		for (int[] arr : result) {
			Arrays.fill(arr, Integer.MAX_VALUE - 100000);
		}

		// First pass: check for left and top
		for (int i = 0; i < m; i++) {
			for (int j = 0; j < n; j++) {
				if (mat[i][j] == 0) {
					result[i][j] = 0;
				} else {
					if (i > 0) {
						result[i][j] = Math.min(result[i][j], result[i - 1][j] + 1);
					}
					if (j > 0) {
						result[i][j] = Math.min(result[i][j], result[i][j - 1] + 1);
					}
				}
			}
		}
		// Second pass: check for right and bottom
		for (int i = m - 1; i >= 0; i--) {
			for (int j = n - 1; j >= 0; j--) {
				if (i < m - 1) {
					result[i][j] = Math.min(result[i][j], result[i + 1][j] + 1);
				}
				if (j < n - 1) {
					result[i][j] = Math.min(result[i][j], result[i][j + 1] + 1);
				}
			}
		}
		return result;
	}

	// BFS: Multi Source
	// We break the problem into 1D. Imagine a row of cells with only 1 water cell.
	// As we move away from the water cell, the heights of the land cells should
	// gradually increase. The height of each land cell can naturally be determined
	// by its distance from the water cell. 4 3 2 1 0 1 2 3, 0=water cells (dist=0).
	// Now if we add a second water cell to the row. Now each land cell's height is
	// determined by its smallest distance to any water cell. This ensures a smoots
	// increase in height as we move away from both water cells.
	// 1 0 1 1 0 1 2 3, 0: water cells (distance = 0). When we extend this to 2D,
	// the concept remains same. For each cell int he grid, we calculate its
	// smallest distance to any water cell and assign that value as its height.
	// > Heights increase smoorthly from waater cells, ensuring the highest peak is
	// at the farthest distance from all water cells. This can be visualized as a
	// "ripple effect" where water cells propagate their distances outward,
	// assigning heights to nearby land cells. This approach worsk for 2 reasons:
	// > It follows the rule that the height difference between 2 adjacent cells is
	// at most 1. This is because the minimum distance to water for any 2
	// neighboring cells cannot differ by more than 1. It's optimal as it ensures
	// that the height of the cells increases consistently as we move farther from
	// water cells, maximizing the highest peak on the map.
	// To find the shortest distance from any cell to a water cell, we use BFS
	// starting from all water cells. When a land cell is reached for the 1st time,
	// its shortest distance to a water cell is set. Algo:
