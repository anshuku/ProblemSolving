596. Classes More Than 5 Students

Table: Courses

+-------------+---------+
| Column Name | Type    |
+-------------+---------+
| student     | varchar |
| class       | varchar |
+-------------+---------+
(student, class) is the primary key (combination of columns with unique values) for this table.
Each row of this table indicates the name of a student and the class in which they are enrolled.
 

Write a solution to find all the classes that have at least five students.

Return the result table in any order.

The result format is in the following example.

 

Example 1:

Input: 
Courses table:
+---------+----------+
| student | class    |
+---------+----------+
| A       | Math     |
| B       | English  |
| C       | Math     |
| D       | Biology  |
| E       | Math     |
| F       | Computer |
| G       | Math     |
| H       | Math     |
| I       | Math     |
+---------+----------+
Output: 
+---------+
| class   |
+---------+
| Math    |
+---------+
Explanation: 
- Math has 6 students, so we include it.
- English has 1 student, so we do not include it.
- Biology has 1 student, so we do not include it.
- Computer has 1 student, so we do not include it.


Solution:

SELECT CLASS FROM COURSES GROUP BY CLASS HAVING COUNT(STUDENT) > 4;

where is used to filter the data before any grouping is done and therefore one cannot use aggregate functions in it. 
having is made to run after grouping so you can use aggregate functions (like count, sum, etc.) in it.

-- Can use subquery in table source

SELECT CLASS FROM
(SELECT CLASS, COUNT(CLASS) AS cnt
FROM COURSES
GROUP BY CLASS) x
WHERE x.cnt >= 5;


